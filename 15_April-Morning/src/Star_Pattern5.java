
public class Star_Pattern5 {
    public static void main(String[] args) {
		
    	int ss=7;
    	for(int x=1; x<=ss; x++)
    	{
    			for(int y=1; y<=ss; y++)
    			{
    				System.out.print(" ");
    			}
    			for(int z=1; z<=x; z++)
    			    {
    				System.out.print("*");
    				}
    			System.out.println();
    	}
	}
}
