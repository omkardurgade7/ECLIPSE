
public class Star_Pattern3 {
     public static void main(String[] args) {
		
    	 int rw=5;
    	 
    	 for(int i=1; i<=rw; i++)
    	 {
    		 for(int a=1; a<=rw; a++)
    		 {
    			 System.out.print(" ");
    		 }
    	 
    	 for(int y=1; y<=i; y++)
    	 {
    		 System.out.print("*");
    	 }
    	 System.out.println();
	}
}
}