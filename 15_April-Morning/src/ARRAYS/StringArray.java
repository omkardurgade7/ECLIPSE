package ARRAYS;

public class StringArray {

	
	public static void main(String[] args) 
	{	
		//Step-I: Array Declaration
	     String[] Str1=new String[4];
	
	//Step-II: Array Initialization
	     Str1[0]="Rohit";
	     Str1[1]="Virat";
	     Str1[2]="Hardik"; 
	     Str1[3]="MSD";
	
	//Step-III: Usage
	
	System.out.println( Str1[0]);  //Rohit	
	System.out.println( Str1[1]);	 //Virat	
	System.out.println( Str1[3]);	//MSD
	System.out.println( Str1[2]);  //Hardik
		
	}
}
