package ARRAYS;

public class CharArray {

	
	public static void main(String[] args) {
		
		
		char[] grade=new char[4];
		
		grade[0]='A';
		grade[1]='B';
		grade[2]='A';
		grade[3]='a';
		
		System.out.println(grade[0]);
		System.out.println(grade[1]+"+");
		System.out.println(grade[2]);
		System.out.println(grade[3]);
	}
}


