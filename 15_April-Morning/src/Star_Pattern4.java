
public class Star_Pattern4 {

	
	   public static void main(String[] args) {
		
		   int kk=6;
		   
		   for(int a=1; a<=kk; a++)
		   {
			for(int d=1; d<=kk; d++)   
			{
				System.out.print(" ");
			}
			for(int v=1; v<=a; v++)
			{
				System.out.print("*");
			}
			System.out.println();
			}
			}
}
